package com.horse.jk_bms.data.local.entity

import androidx.room.Entity
import androidx.room.PrimaryKey
import com.horse.jk_bms.model.BmsConfig

@Entity(tableName = "config")
data class ConfigEntity(
    @PrimaryKey(autoGenerate = true) val id: Long = 0,
    val timestamp: Long = System.currentTimeMillis(),
    val volSmartSleep: Float,
    val volCellUV: Float,
    val volCellUVPR: Float,
    val volCellOV: Float,
    val volCellOVPR: Float,
    val volBalanTrig: Float,
    val volSOCP100: Float,
    val volSOCP0: Float,
    val volCellRCV: Float,
    val volCellRFV: Float,
    val volSysPwrOff: Float,
    val timBatCOC: Float,
    val timBatCOCPDly: Long,
    val timBatCOCPRDly: Long,
    val timBatDcOC: Float,
    val timBatDcOCPDly: Long,
    val timBatDcOCPRDly: Long,
    val timBatSCPRDly: Long,
    val curBalanMax: Float,
    val tmpBatCOT: Float,
    val tmpBatCOTPR: Float,
    val tmpBatDcOT: Float,
    val tmpBatDcOTPR: Float,
    val tmpBatCUT: Float,
    val tmpBatCUTPR: Float,
    val tmpMosOT: Float,
    val tmpMosOTPR: Float,
    val cellCount: Long,
    val batChargeEn: Long,
    val batDischargeEn: Long,
    val balanEn: Long,
    val capBatCell: Float,
    val scpDelay: Long,
    val volStartBalan: Float,
    val cellConWireRes: String,
    val devAddr: Long,
    val dischrgPreChrgT: Long,
    val currentRange: Float,
    val switchStatus: String,
    val tmpStartHeating: Int,
    val tmpStopHeating: Int,
    val timeSmartSleep: Int,
    val enableFlags: String,
    val tmpBatDCHUT: Int,
    val tmpBatDCHUTPR: Int,
)

fun BmsConfig.toEntity(): ConfigEntity = ConfigEntity(
    volSmartSleep = volSmartSleep,
    volCellUV = volCellUV,
    volCellUVPR = volCellUVPR,
    volCellOV = volCellOV,
    volCellOVPR = volCellOVPR,
    volBalanTrig = volBalanTrig,
    volSOCP100 = volSOCP100,
    volSOCP0 = volSOCP0,
    volCellRCV = volCellRCV,
    volCellRFV = volCellRFV,
    volSysPwrOff = volSysPwrOff,
    timBatCOC = timBatCOC,
    timBatCOCPDly = timBatCOCPDly,
    timBatCOCPRDly = timBatCOCPRDly,
    timBatDcOC = timBatDcOC,
    timBatDcOCPDly = timBatDcOCPDly,
    timBatDcOCPRDly = timBatDcOCPRDly,
    timBatSCPRDly = timBatSCPRDly,
    curBalanMax = curBalanMax,
    tmpBatCOT = tmpBatCOT,
    tmpBatCOTPR = tmpBatCOTPR,
    tmpBatDcOT = tmpBatDcOT,
    tmpBatDcOTPR = tmpBatDcOTPR,
    tmpBatCUT = tmpBatCUT,
    tmpBatCUTPR = tmpBatCUTPR,
    tmpMosOT = tmpMosOT,
    tmpMosOTPR = tmpMosOTPR,
    cellCount = cellCount,
    batChargeEn = batChargeEn,
    batDischargeEn = batDischargeEn,
    balanEn = balanEn,
    capBatCell = capBatCell,
    scpDelay = scpDelay,
    volStartBalan = volStartBalan,
    cellConWireRes = com.horse.jk_bms.data.local.Converters.fromFloatArray(cellConWireRes),
    devAddr = devAddr,
    dischrgPreChrgT = dischrgPreChrgT,
    currentRange = currentRange,
    switchStatus = com.horse.jk_bms.data.local.Converters.fromBooleanArray(switchStatus),
    tmpStartHeating = tmpStartHeating,
    tmpStopHeating = tmpStopHeating,
    timeSmartSleep = timeSmartSleep,
    enableFlags = com.horse.jk_bms.data.local.Converters.fromByteArray(enableFlags),
    tmpBatDCHUT = tmpBatDCHUT,
    tmpBatDCHUTPR = tmpBatDCHUTPR,
)
