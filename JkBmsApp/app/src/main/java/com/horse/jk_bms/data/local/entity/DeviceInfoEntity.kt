package com.horse.jk_bms.data.local.entity

import androidx.room.Entity
import androidx.room.PrimaryKey
import com.horse.jk_bms.model.BmsDeviceInfo

@Entity(tableName = "device_info")
data class DeviceInfoEntity(
    @PrimaryKey(autoGenerate = true) val id: Long = 0,
    val timestamp: Long = System.currentTimeMillis(),
    val manuDeviceID: String,
    val maxCells: Int,
    val hardwareVersion: String,
    val softwareVersion: String,
    val oddRunTime: Long,
    val pwrOnTimes: Long,
    val bluetoothName: String,
    val bluetoothPwd: String,
    val manufactureDate: String,
    val deviceSN: String,
    val userData: String,
    val settingPassword: String,
    val userData2: String,
    val cmdSupportFlags: String,
    val agencyId: Int,
    val uart1ProtoNo: Int,
    val canProtoNo: Int,
    val uart1ProtoEnabled: String,
    val hardwareOption: String,
    val canProtoEnabled: String,
    val uart2ProtoNo: Int,
    val uart2ProtoEnabled: String,
    val lcdBuzzerTrigger: Int,
    val dry1Trigger: Int,
    val dry2Trigger: Int,
    val uartMPTLVer: Int,
    val lcdBuzzerTriggerVal: Int,
    val lcdBuzzerReleaseVal: Int,
    val dry1TriggerVal: Int,
    val dry1ReleaseVal: Int,
    val dry2TriggerVal: Int,
    val dry2ReleaseVal: Int,
    val dataStoredPeriod: Long,
    val rcvTime: Float,
    val rfvTime: Float,
    val canMPTLVer: Int,
    val emergencyTime: Int,
    val uart3ProtoNo: Int,
    val uart3ProtoEnabled: String,
    val reBulkSOC: Int,
    val enableFlags: String,
    val protocolVer: Int,
)

fun BmsDeviceInfo.toEntity(): DeviceInfoEntity = DeviceInfoEntity(
    manuDeviceID = manuDeviceID,
    maxCells = maxCells,
    hardwareVersion = hardwareVersion,
    softwareVersion = softwareVersion,
    oddRunTime = oddRunTime,
    pwrOnTimes = pwrOnTimes,
    bluetoothName = bluetoothName,
    bluetoothPwd = bluetoothPwd,
    manufactureDate = manufactureDate,
    deviceSN = deviceSN,
    userData = userData,
    settingPassword = settingPassword,
    userData2 = userData2,
    cmdSupportFlags = com.horse.jk_bms.data.local.Converters.fromByteArray(cmdSupportFlags),
    agencyId = agencyId,
    uart1ProtoNo = uart1ProtoNo,
    canProtoNo = canProtoNo,
    uart1ProtoEnabled = com.horse.jk_bms.data.local.Converters.fromByteArray(uart1ProtoEnabled),
    hardwareOption = hardwareOption,
    canProtoEnabled = com.horse.jk_bms.data.local.Converters.fromByteArray(canProtoEnabled),
    uart2ProtoNo = uart2ProtoNo,
    uart2ProtoEnabled = com.horse.jk_bms.data.local.Converters.fromByteArray(uart2ProtoEnabled),
    lcdBuzzerTrigger = lcdBuzzerTrigger,
    dry1Trigger = dry1Trigger,
    dry2Trigger = dry2Trigger,
    uartMPTLVer = uartMPTLVer,
    lcdBuzzerTriggerVal = lcdBuzzerTriggerVal,
    lcdBuzzerReleaseVal = lcdBuzzerReleaseVal,
    dry1TriggerVal = dry1TriggerVal,
    dry1ReleaseVal = dry1ReleaseVal,
    dry2TriggerVal = dry2TriggerVal,
    dry2ReleaseVal = dry2ReleaseVal,
    dataStoredPeriod = dataStoredPeriod,
    rcvTime = rcvTime,
    rfvTime = rfvTime,
    canMPTLVer = canMPTLVer,
    emergencyTime = emergencyTime,
    uart3ProtoNo = uart3ProtoNo,
    uart3ProtoEnabled = com.horse.jk_bms.data.local.Converters.fromByteArray(uart3ProtoEnabled),
    reBulkSOC = reBulkSOC,
    enableFlags = com.horse.jk_bms.data.local.Converters.fromByteArray(enableFlags),
    protocolVer = protocolVer,
)
